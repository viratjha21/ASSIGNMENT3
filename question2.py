# Write a python program to print the sum and product of the first 10 natural number using for and while loop
# Sum using for loop:
sum=0
for i in range(1,11):
    sum=sum+i
print(sum)

# sum using while loop:
i=1
while i<=10:
    sum=sum+i
    i=i+1
print(sum)

# product using for loop:
product=1
for i in range(1,11):
    product=product*i
print(product)

